#data types
#
# Text Type:	str
# Numeric Types:	int, float, complex
# Sequence Types:	list, tuple, range
# Mapping Type:	dict
# Set Types:	set, frozenset
# Boolean Type:	bool
# Binary Types:	bytes, bytearray, memoryview

varStr = "Hello World"	#str
varInt = 20	#int
varFloat = 20.5	#float
varComplex = 1j	#complex
varList = ["apple", "banana", "cherry"]	#list
varTuple = ("apple", "banana", "cherry")	#tuple
varRange = range(6)	#range
varDict = {"name": "John", "age": 36}	#dict
varSet = {"apple", "banana", "cherry"}	#set
varFrozenSet = frozenset({"apple", "banana", "cherry"})	#frozenset
varBoolean = True	#bool
varBytes = b"Hello"	#bytes
varByteArray = bytearray(5)	#bytearray
varMemoryView = memoryview(bytes(5))	#memoryview
print()
